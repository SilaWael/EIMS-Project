def t(key, lang=None):
    """Translate a key. Falls back to English, then to the key itself."""
    if lang is None or lang not in TRANSLATIONS:
        lang = DEFAULT_LANG
    return TRANSLATIONS[lang].get(key, TRANSLATIONS[DEFAULT_LANG].get(key, key))


def is_rtl(lang):
    """Returns True if the language is right-to-left."""
    return lang in ('ar', 'he', 'fa', 'ur')


def get_lang_direction(lang):
    return 'rtl' if is_rtl(lang) else 'ltr'


